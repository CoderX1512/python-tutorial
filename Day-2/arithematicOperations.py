num1 = 10
num2 = 5

# addition
add = num1 + num2
print("Sum:", add)

# subtraction
sub = num1 - num2
print("Sub:", sub)

# multiplication
mult = num1 * num2
print("Product:", mult)

# division
div = num1 / num2
print("Division:", div)

# floor division (rounded down)
floor_div = num1 // num2
print("Floor Division:", floor_div)

# modulo (gives remainder)
mod = num1 % num2
print("Modulo:", mod)

# Exponentiation
exp = num1 ** num2
print("Exponentiation :", exp)


